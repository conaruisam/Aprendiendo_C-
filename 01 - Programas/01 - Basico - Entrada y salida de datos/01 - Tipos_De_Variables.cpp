// Tipos de datos básicos de C++

#include<iostream>

using namespace std;

int main(){
	
	// Este es una variable de un número entero.
	int numero = 15;
	// Este tipo de variable son números con decimales
	float flotante = 10.45;
	// Este tipo de variable son números con decimales pero  se pueden almacenar números más grandes.
	double flotanteGrande = 16.55555556;
	// Este tipo de variable es un caracter
	char letrita = 'A';
	
	cout<<numero<<endl;
	cout<<flotante<<endl;
	cout<<flotanteGrande<<endl;
	cout<<letrita<<endl;
	
	return 0;
}
